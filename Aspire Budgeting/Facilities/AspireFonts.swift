import SwiftUI

struct AspireFonts {
  static func rubikRegular(size: CGFloat) -> Font {
    return Font.custom("Rubik-Regular", size: size)
  }
  
  static func rubikLight(size: CGFloat) -> Font {
    return Font.custom("Rubik-Light", size: size)
  }
}
